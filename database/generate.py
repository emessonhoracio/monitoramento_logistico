# I want to generate .csv files for each of bases table (regioes, clientes, motoristas, veiculos)
import csv
import random
from datetime import datetime, timedelta
from pathlib import Path

def salvar_csv(caminho_arquivo, cabecalho, linhas):
    with open(caminho_arquivo, "w", newline="", encoding="utf-8") as arquivo:
        writer = csv.writer(arquivo)
        writer.writerow(cabecalho)
        writer.writerows(linhas)

def gerar_timestamp_aleatorio(rng, inicio, fim):
    intervalo_segundos = int((fim - inicio).total_seconds())
    return inicio + timedelta(seconds=rng.randint(0, intervalo_segundos))

def formatar_timestamp(valor):
    return valor.strftime("%Y-%m-%d %H:%M:%S")

def gerar_entregas(quantidade=10_000, seed=42):
    rng = random.Random(seed)
    fim_periodo = (datetime.now() - timedelta(days=21)).replace(second=0, microsecond=0)
    inicio_periodo = fim_periodo - timedelta(days=730)

    clientes_ids = list(range(1, len(clientes) + 1))
    motoristas_ids = list(range(1, len(motoristas) + 1))
    veiculos_info = [
        {
            "id": indice,
            "capacidade_kg": capacidade_kg,
        }
        for indice, (_, _, capacidade_kg) in enumerate(veiculos, start=1)
    ]

    capacidade_maxima = max(veiculo["capacidade_kg"] for veiculo in veiculos_info)
    disponibilidade_motoristas = {
        motorista_id: inicio_periodo + timedelta(hours=rng.randint(0, 10))
        for motorista_id in motoristas_ids
    }
    disponibilidade_veiculos = {
        veiculo["id"]: inicio_periodo + timedelta(hours=rng.randint(0, 10))
        for veiculo in veiculos_info
    }
    odometro_veiculos = {
        veiculo["id"]: round(rng.uniform(20_000, 120_000), 2)
        for veiculo in veiculos_info
    }

    pedidos = []
    for _ in range(quantidade):
        cliente_id = rng.choice(clientes_ids)
        peso_carga_kg = round(rng.triangular(35.0, capacidade_maxima * 0.92, 420.0), 2)
        quantidade_itens = max(
            1,
            min(240, int(round(peso_carga_kg / rng.uniform(4.0, 35.0)))),
        )
        data_pedido = gerar_timestamp_aleatorio(rng, inicio_periodo, fim_periodo)
        tempo_preparo_horas = rng.uniform(0.5, 18.0)
        distancia_base_km = 30 + ((cliente_id * 37) % 260)
        distancia_km = round(
            rng.triangular(10.0, distancia_base_km * 1.8, distancia_base_km),
            2,
        )

        pedidos.append(
            {
                "cliente_id": cliente_id,
                "data_pedido": data_pedido,
                "tempo_preparo_horas": tempo_preparo_horas,
                "distancia_km": distancia_km,
                "peso_carga_kg": peso_carga_kg,
                "quantidade_itens": quantidade_itens,
            }
        )

    pedidos.sort(key=lambda pedido: pedido["data_pedido"])

    entregas = []
    for indice, pedido in enumerate(pedidos, start=1):
        veiculos_compativeis = [
            veiculo
            for veiculo in veiculos_info
            if veiculo["capacidade_kg"] >= pedido["peso_carga_kg"]
        ]

        melhor_veiculo = None
        melhor_motorista = None
        melhor_disponibilidade = None

        for veiculo in veiculos_compativeis:
            for motorista_id in motoristas_ids:
                disponibilidade_par = max(
                    disponibilidade_veiculos[veiculo["id"]],
                    disponibilidade_motoristas[motorista_id],
                )
                criterio = (
                    disponibilidade_par,
                    veiculo["capacidade_kg"],
                    motorista_id,
                )

                if melhor_disponibilidade is None or criterio < melhor_disponibilidade:
                    melhor_disponibilidade = criterio
                    melhor_veiculo = veiculo
                    melhor_motorista = motorista_id

        data_saida_planejada = pedido["data_pedido"] + timedelta(
            hours=pedido["tempo_preparo_horas"]
        )
        data_saida = max(data_saida_planejada, melhor_disponibilidade[0])

        velocidade_media_kmh = rng.uniform(45.0, 68.0)
        duracao_base_horas = max(
            2.0,
            pedido["distancia_km"] / velocidade_media_kmh
            + pedido["quantidade_itens"] / rng.uniform(18.0, 32.0),
        )
        margem_prazo_horas = rng.uniform(2.0, 12.0)
        prazo_entrega = data_saida + timedelta(
            hours=duracao_base_horas + margem_prazo_horas
        )

        if rng.random() < 0.16:
            atraso_horas = rng.uniform(1.0, margem_prazo_horas + 18.0)
            data_entrega = prazo_entrega + timedelta(hours=atraso_horas)
        else:
            adiantamento_horas = rng.uniform(
                0.0,
                min(margem_prazo_horas * 0.9, 8.0),
            )
            data_entrega = prazo_entrega - timedelta(hours=adiantamento_horas)

        tempo_minimo_viagem = timedelta(hours=max(1.0, duracao_base_horas * 0.7))
        if data_entrega <= data_saida:
            data_entrega = data_saida + tempo_minimo_viagem

        km_inicial = odometro_veiculos[melhor_veiculo["id"]]
        km_final = round(km_inicial + pedido["distancia_km"], 2)
        custo_entrega = round(
            45.0
            + pedido["distancia_km"] * (2.15 + melhor_veiculo["capacidade_kg"] / 10_000)
            + pedido["peso_carga_kg"] * 0.18
            + pedido["quantidade_itens"] * 0.65,
            2,
        )

        pedido_codigo = f"PED-{pedido['data_pedido']:%Y%m%d}-{indice:05d}"
        entregas.append(
            (
                pedido_codigo,
                pedido["cliente_id"],
                melhor_motorista,
                melhor_veiculo["id"],
                formatar_timestamp(pedido["data_pedido"]),
                formatar_timestamp(data_saida),
                formatar_timestamp(prazo_entrega),
                formatar_timestamp(data_entrega),
                f"{km_inicial:.2f}",
                f"{km_final:.2f}",
                f"{custo_entrega:.2f}",
                pedido["quantidade_itens"],
                f"{pedido['peso_carga_kg']:.2f}",
            )
        )

        odometro_veiculos[melhor_veiculo["id"]] = km_final
        disponibilidade_veiculos[melhor_veiculo["id"]] = data_entrega + timedelta(
            hours=rng.uniform(0.5, 3.0)
        )
        disponibilidade_motoristas[melhor_motorista] = data_entrega + timedelta(
            hours=rng.uniform(6.0, 14.0)
        )

    return entregas

def main():
    pasta_saida = Path(__file__).resolve().parent / "csv"
    pasta_saida.mkdir(exist_ok=True)

    # regioes.csv
    regioes_linhas = [(cidade,) for cidade in cidades_alagoas]
    salvar_csv(
        pasta_saida / "regioes.csv",
        ["nome"],
        regioes_linhas
    )

    # clientes.csv
    salvar_csv(
        pasta_saida / "clientes.csv",
        ["nome", "cnpj", "regiao", "ativo"],
        clientes
    )

    # motoristas.csv
    motoristas_linhas = [(nome, True) for nome in motoristas]
    salvar_csv(
        pasta_saida / "motoristas.csv",
        ["nome", "ativo"],
        motoristas_linhas
    )

    # veiculos.csv
    veiculos_linhas = [(placa, modelo, capacidade_kg, True) for placa, modelo, capacidade_kg in veiculos]
    salvar_csv(
        pasta_saida / "veiculos.csv",
        ["placa", "modelo", "capacidade_kg", "ativo"],
        veiculos_linhas
    )

    entregas_linhas = gerar_entregas()
    salvar_csv(
        pasta_saida / "entregas.csv",
        [
            "pedido_codigo",
            "cliente_id",
            "motorista_id",
            "veiculo_id",
            "data_pedido",
            "data_saida",
            "prazo_entrega",
            "data_entrega",
            "km_inicial",
            "km_final",
            "custo_entrega",
            "quantidade_itens",
            "peso_carga_kg",
        ],
        entregas_linhas
    )

    print("Arquivos CSV gerados com sucesso em:", pasta_saida.resolve())

if __name__ == "__main__":
    main()
